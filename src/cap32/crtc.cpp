/* Shadow RAM and fast read banks for SRAM access */
extern byte ram_shadow[];
extern byte *membank_read_fast[];

static inline byte getRAMByte(int video_address) {
   /* Video RAM is always in lower 64KB — read from shadow */
   if ((unsigned)video_address < 65536u)
      return ram_shadow[video_address];
   return *(pbRAM + video_address);
}

static inline word __attribute__((always_inline)) getRAMWord(int video_address) {
   if (__builtin_expect((unsigned)video_address < 65534u, 1))
      return *reinterpret_cast<const word *>(ram_shadow + video_address);
   return *reinterpret_cast<const word *>(pbRAM + video_address);
}


void __attribute__((section(".time_critical.crtc"))) prerender_normal()
{
   /* Read both video bytes in one 16-bit PSRAM access instead of two 8-bit */
   word wVidMem = getRAMWord(CRTC.next_address);
   byte bVidMem0 = (byte)(wVidMem);
   byte bVidMem1 = (byte)(wVidMem >> 8);
   *RendPos = *(ModeMap + (bVidMem0 * 2));
   *(RendPos + 1) = *(ModeMap + (bVidMem0 * 2) + 1);
   *(RendPos + 2) = *(ModeMap + (bVidMem1 * 2));
   *(RendPos + 3) = *(ModeMap + (bVidMem1 * 2) + 1);
   RendPos += 4;
}

/* CPC Plus prerender with soft-scroll support (full width) */
void __attribute__((section(".time_critical.crtc"))) prerender_normal_plus()
{
   unsigned int next_address = CRTC.next_address;
   if (__builtin_expect(asic.vscroll != 0, 0)) {
      if (CRTC.raster_count + asic.vscroll <= (unsigned)CRTC.registers[9]) {
         next_address += asic.vscroll * 0x0800;
      } else {
         next_address += 80;
         next_address -= ((CRTC.registers[9] + 1 - asic.vscroll) * 0x0800);
      }
   }
   word wVidMem = getRAMWord(next_address);
   byte bVidMem0 = (byte)(wVidMem);
   byte bVidMem1 = (byte)(wVidMem >> 8);
   *RendPos = *(ModeMap + (bVidMem0 * 2));
   *(RendPos + 1) = *(ModeMap + (bVidMem0 * 2) + 1);
   *(RendPos + 2) = *(ModeMap + (bVidMem1 * 2));
   *(RendPos + 3) = *(ModeMap + (bVidMem1 * 2) + 1);
   RendPos += 4;
}

/* CPC Plus prerender with soft-scroll support (half width) */
void __attribute__((section(".time_critical.crtc"))) prerender_normal_half_plus()
{
   unsigned int next_address = CRTC.next_address;
   if (__builtin_expect(asic.vscroll != 0, 0)) {
      if (CRTC.raster_count + asic.vscroll <= (unsigned)CRTC.registers[9]) {
         next_address += asic.vscroll * 0x0800;
      } else {
         next_address += 80;
         next_address -= ((CRTC.registers[9] + 1 - asic.vscroll) * 0x0800);
      }
   }
   word wVidMem = getRAMWord(next_address);
   byte bVidMem0 = (byte)(wVidMem);
   byte bVidMem1 = (byte)(wVidMem >> 8);
   *RendPos = *(ModeMap + bVidMem0);
   *(RendPos + 1) = *(ModeMap + bVidMem1);
   RendPos += 2;
}


void set_prerender()
{
   LastPreRend = flags1.combined;
   if (LastPreRend == 0x03ff0000) {
      PreRender = CPC.scr_prerendernorm;
   } else {
      if (!static_cast<word>(LastPreRend)) {
         PreRender = CPC.scr_prerenderbord;
      } else {
         PreRender = CPC.scr_prerendersync;
      }
   }
}


static inline __attribute__((always_inline)) unsigned int getPixel()
{
   return GateArray.palette[*RendOut++];
}


void __attribute__((section(".time_critical.crtc"))) render8bpp()
{
   /* Half-width render: write every other pixel through byte palette */
   byte bCount = *RendWid++;
   const byte *rend = RendOut;
   RendOut += bCount;
   byte *out = CPC.scr_pos;
   int half = bCount >> 1;
   int i = 0;
   for (; i + 3 < half; i += 4) {
      out[0] = palette_byte[rend[0]];
      out[1] = palette_byte[rend[2]];
      out[2] = palette_byte[rend[4]];
      out[3] = palette_byte[rend[6]];
      out += 4;
      rend += 8;
   }
   for (; i < half; i++) {
      *out++ = palette_byte[*rend];
      rend += 2;
   }
   CPC.scr_pos = out;
}


/* Combined prerender + render for the common case (normal display, 8bpp).
 * Reads video RAM, decodes through ModeMap, writes half-width output
 * directly to scr_pos using the byte palette cache — no RendBuff round-trip. */
static inline void __attribute__((always_inline, section(".time_critical.crtc")))
prerender_and_render_normal_8bpp()
{
   word wVidMem = getRAMWord(CRTC.next_address);
   byte bVidMem0 = (byte)(wVidMem);
   byte bVidMem1 = (byte)(wVidMem >> 8);
   
   /* Fast path for modes 0/1: use precomputed LUT (2 dword stores).
    * Skip RendPos/RendWid/RendOut — they're reset at scanline start anyway. */
   const uint32_t *lut = active_halfwidth_lut;
   if (__builtin_expect(lut != nullptr, 1)) {
      byte *out = CPC.scr_pos;
      uint32_t v0 = lut[bVidMem0];
      uint32_t v1 = lut[bVidMem1];
      memcpy(out, &v0, 4);
      memcpy(out + 4, &v1, 4);
      CPC.scr_pos = out + 8;
      return;
   }
   
   /* Generic path: advance RendPos/RendWid/RendOut + ModeMap decode */
   RendPos += 4;
   byte bCount = *RendWid++;
   RendOut += bCount;
   const dword *map0 = ModeMap + (bVidMem0 * 2);
   const dword *map1 = ModeMap + (bVidMem1 * 2);
   dword pens0 = map0[0];
   dword pens1 = map0[1];
   dword pens2 = map1[0];
   dword pens3 = map1[1];
   
   byte *out = CPC.scr_pos;
   byte b0 = palette_byte[pens0 & 0xFF];
   byte b1 = palette_byte[(pens0 >> 16) & 0xFF];
   byte b2 = palette_byte[pens1 & 0xFF];
   byte b3 = palette_byte[(pens1 >> 16) & 0xFF];
   uint32_t w0 = b0 | (b1 << 8) | (b2 << 16) | (b3 << 24);
   memcpy(out, &w0, 4);
   byte b4 = palette_byte[pens2 & 0xFF];
   byte b5 = palette_byte[(pens2 >> 16) & 0xFF];
   byte b6 = palette_byte[pens3 & 0xFF];
   byte b7 = palette_byte[(pens3 >> 16) & 0xFF];
   uint32_t w1 = b4 | (b5 << 8) | (b6 << 16) | (b7 << 24);
   memcpy(out + 4, &w1, 4);
   CPC.scr_pos = out + 8;
}

void __attribute__((hot, section(".time_critical.crtc"))) crtc_cycle(int repeat_count)
{
   do {
      if (scrln_visible) { // visible scanline?
         if (HorzChar < HorzMax) {
            if (__builtin_expect(flags1.combined != LastPreRend, 0)) {
               set_prerender(); // change pre-renderer if necessary
            }
            /* Fast path: if in normal display mode, use combined prerender+render */
            if (__builtin_expect(LastPreRend == 0x03ff0000, 1)) {
               prerender_and_render_normal_8bpp();
            } else {
               /* Border/sync path: write border color directly, skip RendBuff round-trip */
               byte borderColor = (static_cast<word>(LastPreRend) == 0) ?
                  palette_byte[16] : palette_byte[17]; /* 16=border, 17=sync */
               byte bCount = *RendWid++;
               RendOut += bCount;
               /* Advance RendPos to keep in sync (4 dwords = 16 bytes) */
               RendPos += 4;
               /* Write half-width border to scr_pos */
               byte *out = CPC.scr_pos;
               int half = bCount >> 1;
               memset(out, borderColor, half);
               CPC.scr_pos = out + half;
            }
         }
      }
      /* Compute MAXlate inline — prepares next_address for the NEXT iteration's render.
       * Must run every iteration to maintain correct timing. */
      {
         unsigned int idx = (CRTC.addr + CRTC.char_count) & 0x73ff;
         unsigned int j = idx << 1;
         CRTC.next_address = ((j & 0x7FE) | ((j & 0x6000) << 1)) | CRTC.scr_base;
      }
      flags1.dt.combined = new_dt.combined; // update the DISPTMG flags

      #ifdef DEBUG_CRTC
      if (dwDebugFlag) {
         char str[16];
         char on[]  = "vhDDHVMa";
         char off[] = "........";

         if (CRTC.flag_invsync) {
            str[0] = on[0];
         } else {
            str[0] = off[0];
         }
         if (flags1.inHSYNC) {
            str[1] = on[1];
         } else {
            str[1] = off[1];
         }
         if (flags1.dt.HDSPTIMG & 1) {
            str[2] = on[2];
         } else {
            str[2] = off[2];
         }
         if (flags1.dt.DISPTIMG) {
            str[3] = on[3];
         } else {
            str[3] = off[3];
         }
         if (CRTC.flag_newscan) {
            str[4] = on[4];
         } else {
            str[4] = off[4];
         }
         if (CRTC.flag_resframe) {
            str[5] = on[5];
         } else {
            str[5] = off[5];
         }
         if (CRTC.flag_resscan) {
            str[6] = on[6];
         } else {
            str[6] = off[6];
         }
         if (CRTC.flag_invta) {
            str[7] = on[7];
         } else {
            str[7] = off[7];
         }
         str[8] = '\0';

         fprintf(pfoDebug, "%04X | CC:%2X RC:%2X LC:%2X - HSWC:%2X VSWC:%2X - %s %c %2X  mhs%04X mfs%04X  s%04X p%04X e%04X p%04X c%d\r\n",
            CRTC.next_address,
            CRTC.char_count,
            CRTC.raster_count,
            CRTC.line_count,
            CRTC.hsw_count,
            CRTC.vsw_count,
            str,
            z80.int_pending ? 'I' : '.',
            GateArray.sl_count,
            MonHSYNC,
            MonFreeSync,
            iMonHSStartPos,
            iMonHSPeakPos,
            iMonHSEndPos,
            HorzPos,
            HorzChar);
      }
      #endif

      iMonHSStartPos += 0x100;
      iMonHSEndPos += 0x100;
      iMonHSPeakPos += 0x100;
      HorzPos += 0x100;
      HorzChar++;
      if (__builtin_expect(HorzPos >= MonHSYNC, 0)) {
         if (VDU.flag_drawing) {
            if (scrln_visible) {
               extern void scanline_complete(int scrln);
               scanline_complete(VDU.scrln);
            }
            CPC.scr_base += CPC.scr_line_offs; // advance surface pointer to next row
         }
         HadP = 1;
         iMonHSPeakPos = HorzPos - MonHSYNC;
         iMonHSStartToPeak = iMonHSStartPos - iMonHSPeakPos;
         iMonHSEndToPeak = iMonHSEndPos - iMonHSPeakPos;
         HorzPos = iMonHSPeakPos - HSyncDuration;

         HorzChar = HorzPos >> 8;
         dword val = (HorzPos & 0xf0) >> PosShift;
         val &= ~3u; // ARM: align to 4 bytes for dword access via RendPos
         if (!val) {
            HorzMax = 48;
            HorzPix[0] = HorzPix[1];
            RendPos = RendStart;
            HorzChar--;
         } else {
            RendPos = reinterpret_cast<dword *>(&RendBuff[val]);
            int tmp = reinterpret_cast<byte *>(RendStart) - reinterpret_cast<byte *>(RendPos);
            HorzPix[48] = static_cast<byte>(tmp);
            HorzPix[0] = HorzPix[1] - static_cast<byte>(tmp);
            HorzMax = 49;
         }
         RendOut = reinterpret_cast<byte *>(RendStart);
         RendWid = &HorzPix[0];
         CPC.scr_pos = CPC.scr_base;
         VDU.scrln++;
         VDU.scanline++;
         if (static_cast<dword>(VDU.scrln) >= MAX_DRAWN) {
            VDU.flag_drawing = 0;
            scrln_visible = 0;
         } else {
            VDU.flag_drawing = 1;
            int fy = VDU.scrln - crtc_fb_y_start;
            scrln_visible = ((unsigned)fy < (unsigned)CPC_FB_HEIGHT);
         }
      }

// ----------------------------------------------------------------------------

      /* Fast path: if char_count+1 won't hit any event, skip all comparisons. */
      {
         byte next_cc = (CRTC.char_count + 1) & 255;
         if (__builtin_expect(
               next_cc != CRTC.registers[0] &&
               next_cc < next_event_char &&
               !flags1.inHSYNC &&
               CRTC.charInstSL_state == 0 &&
               CRTC.charInstMR_state == 0, 1)) {
            CRTC.char_count = next_cc;
            goto crtc_bottom;
         }
      }

      if (CRTC.char_count == CRTC.registers[0]) { // matches horizontal total?
         CRTC.last_hend = CRTC.char_count; // preserve current line length in chars
         CRTC.flag_newscan = 1; // request starting a new scan line
         CRTC.char_count = 0; // reset the horizontal character count
      } else {
         CRTC.char_count++; // update counter
         CRTC.char_count &= 255; // limit to 8 bits
      }

      if (CRTC.char_count == CRTC.registers[0]) { // matches horizontal total?
         if (CRTC.raster_count == CRTC.registers[9]) { // matches maximum raster address?
            CRTC.flag_reschar = 1; // request a line count update
         } else {
            CRTC.flag_reschar = 0; // still within the current character line
         }
         if (CRTC.flag_resnext) { // ready to restart frame?
            CRTC.flag_resnext = 0;
            CRTC.flag_resframe = 1; // request a frame restart
         }
         if (CRTC.flag_startvta) { // ready to start vertical total adjust?
            CRTC.flag_startvta = 0;
            CRTC.flag_invta = 1; // entering vertical total adjust
         }
         if (CRTC.flag_invta) { // in vertical total adjust?
            if ((CRTC.raster_count == CRTC.registers[9]) && (CRTC.line_count == CRTC.registers[4])) {
               CRTC.flag_resscan = 1; // raster counter only resets once at start of vta
            } else {
               CRTC.flag_resscan = 0; // raster counter keeps increasing while in vta
            }
         }
      }

      if (CRTC.char_count == CRTC.registers[1]) { // matches horizontal displayed?
         if (CRTC.raster_count == CRTC.registers[9]) { // matches maximum raster address?
            CRTC.next_addr = CRTC.addr + CRTC.char_count;
         }
      }

      if (!flags1.inHSYNC) { // not in HSYNC?
         if (CRTC.char_count == CRTC.registers[2]) { // matches horizontal sync position?
            flags1.inHSYNC = 0xff; // turn HSYNC on
            CRTC.flag_hadhsync = 1; // prevent GA from processing more than one HSYNC per scan line
            CRTC.hsw_count = 0; // initialize horizontal sync width counter
            match_hsw();
         }
      } else {
         match_hsw();
      }

      if (__builtin_expect(CRTC.charInstSL_state != 0, 0))
         CRTC.CharInstSL(); // if necessary, process vertical total delay
      if (__builtin_expect(CRTC.charInstMR_state != 0, 0))
         CRTC.CharInstMR(); // if necessary, process maximum raster count delay

      if (__builtin_expect(CRTC.flag_newscan, 0)) { // scanline change requested?
         CRTC.flag_newscan = 0;
         /* CPC Plus: screen split — change CRTC address at specific scanline */
         if (CRTC.split_sl && CRTC.sl_count == CRTC.split_sl) {
            CRTC.next_addr = CRTC.split_addr;
         }
         CRTC.addr = CRTC.next_addr;
         CRTC.sl_count++;

         if (CRTC.flag_invsync) { // VSYNC active?
            CRTC.vsw_count++; // update counter
            CRTC.vsw_count &= 15; // limit to 4 bits
            if (CRTC.vsw_count == CRTC.vsw) { // matches vertical sync width?
               CRTC.vsw_count = 0; // reset counter
               CRTC.flag_resvsync = 1; // request VSYNC reset
            }
         }

         if (CRTC.flag_resframe) { // frame restart requested?
            restart_frame();
         } else {
            if (CRTC.flag_resscan) { // raster counter reset requested?
               CRTC.flag_resscan = 0;
               CRTC.raster_count = 0; // reset counter
               CRTC.scr_base = 0;
            } else {
               CRTC.raster_count++; // update counter
               CRTC.raster_count &= 31; // limit to 5 bits
               if (!CRTC.raster_count) { // did the counter wrap around?
                  match_line_count();
               }
               CRTC.scr_base = (CRTC.scr_base + 0x0800) & 0x3800;
            }
         }

         CRTC.CharInstSL = CharSL1;
         CRTC.charInstSL_state = 1;

         dword temp = 0;
         if (CRTC.raster_count == CRTC.registers[9]) { // matches maximum raster address?
            temp = 1;
            CRTC.flag_resscan = 1; // request a raster counter reset
         }
         if (CRTC.r9match != temp) {
            CRTC.r9match = temp;
         }
         if (temp) {
            CRTC.CharInstMR = CharMR1;
            CRTC.charInstMR_state = 1;
         }

         if (CRTC.flag_invta) { // in vertical total adjust?
            if (CRTC.raster_count == CRTC.reg5) { // matches vertical total adjust?
               restart_frame();
               if (CRTC.registers[9] == 0) { // maximum raster address is zero?
                  CRTC.flag_resscan = 1; // request a raster counter reset
               }
            }
         }

         if (CRTC.flag_reschar) { // line count update requested?
            CRTC.line_count++; // update counter
            CRTC.line_count &= 127; // limit to 7 bits
            reload_addr();
         }

         if (CRTC.flag_invsync) { // in VSYNC?
            if (CRTC.flag_resvsync) { // end of VSYNC?
               CRTC.flag_invsync = 0; // turn VSYNC off
               CRTC.flag_resvsync = 0;
               if (VDU.scanline == MaxVSync) { // above maximum scanline count?
                  frame_finished();
               }
            } else {
               if (VDU.scanline > MinVSync) { // above minimum scanline count?
                  frame_finished();
               }
            }
         } else if (VDU.scanline == MaxVSync) { // above maximum scanline count?
            frame_finished();
         }
      }

      if (CRTC.char_count == CRTC.hstart) { // leaving border area?
         new_dt.NewHDSPTIMG |= 0x01;
      }
      if (CRTC.char_count == CRTC.hend) { // entering border area?
         new_dt.NewHDSPTIMG &= 0xfe;
      }
      recompute_next_event();
crtc_bottom:

// ----------------------------------------------------------------------------

      repeat_count--;
   } while (__builtin_expect(repeat_count > 0, 0));
}


void crtc_init()
{
   dwXScale = 1;
   ModeMaps[0] = M0Map;
   ModeMaps[1] = M1Map;
   ModeMaps[2] = M2Map;
   ModeMaps[3] = M3Map;
   ModeMap = ModeMaps[0];

   CPC.scr_render = render8bpp;
   if (CPC.model > 2) {
      CPC.scr_prerendernorm = prerender_normal_plus;
   } else {
      CPC.scr_prerendernorm = prerender_normal;
   }
   CPC.scr_prerenderbord = prerender_border;
   CPC.scr_prerendersync = prerender_sync;

   PosShift = 4;
   for (int i = 0; i < 48; i++) {
      HorzPix[i] = 16;
   }
   HorzPix[48] = 0;
   RendStart = reinterpret_cast<dword *>(&RendBuff[16]);
}


void crtc_reset()
{
   memset(&CRTC, 0, sizeof(CRTC)); // clear CRTC data structure
   CRTC.registers[0] = 0x3f;
   CRTC.registers[2] = 0x2e;
   CRTC.registers[3] = 0x8e;

   RendPos = reinterpret_cast<dword *>(&RendBuff[0]);
   RendOut = reinterpret_cast<byte *>(RendStart);
   RendWid = &HorzPix[0];

   HorzPos = 0x500;
   HorzChar = 0x04;
   HorzMax = 48;
   HSyncDuration = 0xA00;
   MinHSync = 0x4000 - HSyncDuration - 257;
   MaxHSync = 0x4000 - HSyncDuration + 257;
   MonHSYNC = 0x4000 - HSyncDuration;
   MonFreeSync = MonHSYNC;
   flags1.monVSYNC = 0;
   flags1.dt.DISPTIMG = 0xff;
   flags1.dt.HDSPTIMG = 0x03;
   new_dt.NewDISPTIMG = 0xff;
   new_dt.NewHDSPTIMG = 0x03;
   CRTC.CharInstSL = NoChar;
   CRTC.CharInstMR = NoChar;
   CRTC.charInstSL_state = 0;
   CRTC.charInstMR_state = 0;
   CRTC.split_addr = 0;
   CRTC.split_sl = 0;
   CRTC.sl_count = 0;
   CRTC.interrupt_sl = 0;

   MinVSync = MID_VHOLD;
   MaxVSync = MinVSync + MIN_VHOLD_RANGE +
      (((MinVSync - MIN_VHOLD) * (MAX_VHOLD_RANGE - MIN_VHOLD_RANGE)) +
      (MAX_VHOLD - MIN_VHOLD) - 1) / (MAX_VHOLD - MIN_VHOLD);
   next_event_char = 0; /* force full state machine on first iteration */
}
